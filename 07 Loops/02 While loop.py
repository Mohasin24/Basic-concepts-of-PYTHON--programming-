# While Loop :
#Syntax:

'''  
while condition :
    #Body of the loop
'''

i = 0
while i<=10 :
    print ('Yes ' + str(i)) #This code keep exicuting until the condition is True
    i = i + 1
print('Done!')    

'''In while loop the condition is checked first. 
If it evaluates to true then the body of the loop is executed otherwise not.
If the loop is entered the process of [condition check & execution] is continued until
the condition becomes false.'''