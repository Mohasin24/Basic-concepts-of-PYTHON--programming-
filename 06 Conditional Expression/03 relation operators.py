# Relation Operators : Relation operators are used to evaluate conditions inside the if statement
# Examples:
'''
== :- Equals to equals to
>= :- Greater than or equal to
<= :- less than or equal to

'''

a = 10

if(a==10):
    print('The value of a is 10')
if (a>=7):
    print('The value of a is greater than 7')
if (a<=15):
    print('The value of a is less than 15')
else :
    print('No')    
    